
// star pattern 
let n = 5
for (let i = 0; i <= n; i++) {
    str = ""
    for (let j = 0; j <= i; j++) {
        str += "* "

    }
    // console.log(str);
}
let m = 5;

for (let k = m; k >= 0; k--) {
    let star = "";

    for (let l = 0; l <= k; l++) {
        star += "* ";
    }

    // console.log(star);

}